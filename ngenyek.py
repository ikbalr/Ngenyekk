def vowel(text):
    newstr = text.replace("e", "i")
    newstr1 = newstr.replace("a", "i")
    newstr2 = newstr1.replace("o", "i")
    newstr3 = newstr2.replace("O", "i")
    newstr4 = newstr3.replace("i", "i")
    newstr5 = newstr4.replace("I", "i")
    newstr6 = newstr5.replace("U", "i")
    newstr7 = newstr6.replace("u", "i")
    newstr8 = newstr7.replace("E", "i")
    newstr9 = newstr8.replace("A", "i")
    return newstr9

